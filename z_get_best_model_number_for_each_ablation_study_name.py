def modify_and_run_benchmark(sweep_id: str, epoch: int, ablation_study_name: str) -> bool:
    """
    Modify z_start_benchmark_cluster.py with the sweep_id and epoch, then run it.
    """
    benchmark_path = Path(BENCHMARK_SCRIPT)
    if not benchmark_path.exists():
        print(f"✗ Benchmark script not found: {benchmark_path}")
        return False
    
    # Read the current file
    content = benchmark_path.read_text(encoding='utf-8')
    
    # Find and replace the sweep_id and constrained_model_number lines
    lines = content.split('\n')
    modified_lines = []
    
    for line in lines:
        if line.strip().startswith('sweep_id = '):
            # Preserve the comment if it exists
            if '#' in line:
                comment_part = line[line.index('#'):]
                modified_lines.append(f'sweep_id = "{sweep_id}" {comment_part}')
            else:
                modified_lines.append(f'sweep_id = "{sweep_id}"')
        elif line.strip().startswith('constrained_model_number = '):
            # Preserve the comment if it exists
            if '#' in line:
                comment_part = line[line.index('#'):]
                modified_lines.append(f'constrained_model_number = {int(epoch)}  {comment_part}')
            else:
                modified_lines.append(f'constrained_model_number = {int(epoch)}')
        else:
            modified_lines.append(line)
    
    # Write back the modified content
    modified_content = '\n'.join(modified_lines)
    benchmark_path.write_text(modified_content, encoding='utf-8')
    
    print(f"✓ Modified {BENCHMARK_SCRIPT}:")
    print(f"    sweep_id = \"{sweep_id}\"")
    print(f"    constrained_model_number = {int(epoch)}")
    print(f"    Ablation Study: {ablation_study_name}")
    print()
    
    # Run the benchmark script
    try:
        print(f"⏳ Starting benchmark job...")
        result = subprocess.run([sys.executable, str(benchmark_path)], 
                              capture_output=True, text=True, encoding='utf-8')
        
        if result.returncode != 0:
            print(f"✗ Benchmark script failed:")
            print(result.stderr)
            return False
        
        print(result.stdout)
        print(f"✓ Benchmark job submitted successfully\n")
        print("-" * 80 + "\n")
        return True
        
    except Exception as e:
        print(f"✗ Error running benchmark script: {e}")
        return False


def main():
    """Main execution function."""
    import argparse
    
    parser = argparse.ArgumentParser(
        description="Find best models for each ablation study and launch benchmarks"
    )
    parser.add_argument(
        "--csv-file",
        type=Path,
        default=Path(CSV_FILE),
        help=f"Path to ablation studies CSV file (default: {CSV_FILE})"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Show what would be done without actually starting benchmark jobs"
    )
    parser.add_argument(
        "--auto",
        action="store_true",
        help="Automatically start all benchmark jobs without prompting"
    )
    
    args = parser.parse_args()
    csv_path = args.csv_file
    
    # Check if CSV exists
    if not csv_path.exists():
        print(f"✗ CSV file not found: {csv_path}")
        print(f"  Please ensure the CSV file exists.")
        print(f"  You can generate it by running: python z_ablation_study_postprocess.py")
        sys.exit(1)
    
    # Load data
    df = load_ablation_data(csv_path)
    
    # Check required columns
    required_columns = ['ablation_study_name', 'sweep_id', 'epoch', 'coverage_end']
    missing_columns = [col for col in required_columns if col not in df.columns]
    if missing_columns:
        print(f"✗ Missing required columns in CSV: {', '.join(missing_columns)}")
        print(f"  Available columns: {', '.join(df.columns)}")
        sys.exit(1)
    
    # Find best models
    best_models = find_best_models(df)
    
    # Print results
    print_best_models(best_models)
    
    # Check if running in dry-run mode
    if args.dry_run:
        print("DRY RUN MODE - No benchmark jobs will be started")
        print("\nThe following commands would be executed:\n")
        for idx, row in best_models.iterrows():
            sweep_id = row['sweep_id']
            epoch = int(row['epoch'])
            ablation_study_name = row['ablation_study_name']
            print(f"[{idx + 1}/{len(best_models)}] {ablation_study_name}")
            print(f"  python {BENCHMARK_SCRIPT} --sweep-id {sweep_id}")
            print(f"  (with constrained_model_number = {epoch})")
            print()
        sys.exit(0)
    
    # Ask user if they want to start benchmark jobs (unless --auto flag is set)
    if not args.auto:
        print(f"Found {len(best_models)} best models.")
        response = input("Do you want to start benchmark jobs for all best models? (yes/no): ").strip().lower()
        
        if response not in ['yes', 'y']:
            print("Benchmark jobs not started. Exiting.")
            sys.exit(0)
    else:
        print(f"AUTO MODE: Starting benchmark jobs for {len(best_models)} best models...")
    
    # Start benchmark jobs for each best model
    print("\n" + "=" * 80)
    print("STARTING BENCHMARK JOBS")
    print("=" * 80 + "\n")
    
    success_count = 0
    for idx, row in best_models.iterrows():
        sweep_id = row['sweep_id']
        epoch = int(row['epoch'])
        ablation_study_name = row['ablation_study_name']
        
        print(f"[{idx + 1}/{len(best_models)}] Processing: {ablation_study_name}")
        
        if modify_and_run_benchmark(sweep_id, epoch, ablation_study_name):
            success_count += 1
    
    print("=" * 80)
    print(f"✓ Successfully started {success_count}/{len(best_models)} benchmark jobs")
    print("=" * 80)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\n✗ Interrupted by user")
        sys.exit(1)
    except Exception as e:
        print(f"\n✗ Error: {e}")
        import traceback
        traceback.print_exc()
        sys.exit(1)
