"""
Find the best model (by avg coverage_end, then shortest path_length_end) for each ablation study name,
then launch benchmark jobs for each.

Expected CSV format (generated by z_ablation_study_postprocess.py):
    - ablation_study_name: Name of the ablation study
    - sweep_id: W&B sweep ID
    - epoch: Model epoch number
    - coverage_end: Coverage at end of episode
    - path_length_end: Path length at end of episode
    - map: Map identifier
    - run_number: Run number
    - Other columns: timestamp, model, seed, path_length_90_pct, etc.

The script will:
1. Group data by (ablation_study_name, sweep_id, epoch)
2. Calculate average coverage_end across all maps for each group
3. Find the epoch with highest average coverage_end for each ablation_study_name
4. If multiple epochs have the same max coverage, select the one with shortest path_length_end
5. Launch benchmark jobs for each best model found
"""
from __future__ import annotations

import pandas as pd
import subprocess
import sys
from pathlib import Path
from typing import Dict, Tuple

# Configuration
CSV_FILE = "misc/logs/benchmark_results/ablation_studies_evaluation_files.csv"
BENCHMARK_SCRIPT = "z_start_benchmark_cluster.py"


def load_ablation_data(csv_path: Path) -> pd.DataFrame:
    """Load the ablation studies evaluation CSV file."""
    if not csv_path.exists():
        raise FileNotFoundError(f"CSV file not found: {csv_path}")
    
    df = pd.read_csv(csv_path)
    print(f"✓ Loaded {len(df)} rows from {csv_path.name}")
    return df

def find_best_models(df: pd.DataFrame) -> pd.DataFrame:
    """
    Find the best model for each ablation study name.
    Best model = highest average coverage_end across all maps AND all sweep_ids for that epoch/ablation_study.
    Tiebreaker (when coverage values are close or equal): select the one with shortest path_length_end.
    
    This logic matches vis_for_icaps_paper.py: 
    - Groups by ablation_study_name and epoch only (combining all sweep_ids)
    - Sorts by coverage (descending) then path_length (ascending)
    - After finding best epoch, selects the specific sweep_id with best performance for that epoch
    
    Returns a DataFrame with columns: ablation_study_name, epoch, avg_coverage_end, avg_path_end, sweep_id
    """
    # Convert epoch to numeric if it's not already
    # Handle cases where epoch might be "epoch-X" format
    if df['epoch'].dtype == 'object':
        df['epoch_num'] = df['epoch'].astype(str).str.extract(r'epoch-(\d+)')[0].astype(int)
    else:
        df['epoch_num'] = df['epoch'].astype(int)
    
    # Convert path_length_end to numeric, handling empty strings and non-numeric values
    df['path_length_end'] = pd.to_numeric(df['path_length_end'], errors='coerce')
    
    # Group by ablation_study_name and epoch_num (NOT by sweep_id - combine all sweeps)
    # Calculate mean coverage_end and mean path_length_end across all maps and all sweep_ids for each group
    grouped = df.groupby(['ablation_study_name', 'epoch_num']).agg({
        'coverage_end': 'mean',
        'path_length_end': 'mean',
        'epoch': 'first'
    }).reset_index()
    grouped.rename(columns={
        'coverage_end': 'avg_coverage_end',
        'path_length_end': 'avg_path_end'
    }, inplace=True)
    
    # For each ablation_study_name, find the best epoch
    best_models = []
    for ablation_study in grouped['ablation_study_name'].unique():
        study_data = grouped[grouped['ablation_study_name'] == ablation_study].copy()
        
        # Sort by coverage (descending) then path_length (ascending) as tiebreaker
        # This matches the logic in vis_for_icaps_paper.py
        # Handle NaN values in path_length by filling with infinity
        study_data['path_sort_key'] = study_data['avg_path_end'].fillna(float('inf'))
        study_data_sorted = study_data.sort_values(['avg_coverage_end', 'path_sort_key'], 
                                                    ascending=[False, True])
        
        # Select the best epoch (first row after sorting)
        best_epoch_row = study_data_sorted.iloc[0]
        best_epoch_num = int(best_epoch_row['epoch_num'])
        
        # Check if there were ties in coverage
        max_coverage = best_epoch_row['avg_coverage_end']
        tied_rows = study_data[study_data['avg_coverage_end'] == max_coverage]
        
        if len(tied_rows) > 1:
            print(f"  ℹ Tiebreaker applied for '{ablation_study}': {len(tied_rows)} epochs with coverage {max_coverage:.4f}")
            print(f"    Selected epoch {best_epoch_num} with path_end={best_epoch_row['avg_path_end']:.2f}")
        
        # Now find the specific sweep_id with the best performance for this epoch
        # Get all data for this ablation study and epoch across all sweep_ids
        epoch_data = df[(df['ablation_study_name'] == ablation_study) & 
                        (df['epoch_num'] == best_epoch_num)].copy()
        
        # Group by sweep_id to find which one performs best for this epoch
        sweep_stats = epoch_data.groupby('sweep_id').agg({
            'coverage_end': 'mean',
            'path_length_end': 'mean'
        }).reset_index()
        
        # Sort to find best sweep_id (same logic: max coverage, then min path length)
        sweep_stats['path_sort_key'] = sweep_stats['path_length_end'].fillna(float('inf'))
        sweep_stats_sorted = sweep_stats.sort_values(['coverage_end', 'path_sort_key'], 
                                                      ascending=[False, True])
        best_sweep_id = sweep_stats_sorted.iloc[0]['sweep_id']
        
        # Create the result row
        result_row = {
            'ablation_study_name': ablation_study,
            'sweep_id': best_sweep_id,
            'epoch': best_epoch_num,
            'epoch_name': best_epoch_row['epoch'],
            'avg_coverage_end': best_epoch_row['avg_coverage_end'],
            'avg_path_end': best_epoch_row['avg_path_end']
        }
        
        best_models.append(result_row)
    
    # Create final dataframe
    best_models_df = pd.DataFrame(best_models).reset_index(drop=True)
    
    # Sort by ablation_study_name for better readability
    best_models_df = best_models_df.sort_values('ablation_study_name').reset_index(drop=True)
    
    return best_models_df
    
